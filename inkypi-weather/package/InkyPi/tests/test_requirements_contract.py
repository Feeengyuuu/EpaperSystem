def test_ci_actions_are_sha_pinned_and_jobs_are_bounded():
    workflows = [REPO_ROOT / ".github/workflows/test.yml", REPO_ROOT / ".github/workflows/security.yml"]
    for workflow in workflows:
        text = workflow.read_text(encoding="utf-8")
        assert "permissions:\n  contents: read" in text
        assert "timeout-minutes:" in text
        action_refs = re.findall(r"^\s*-?\s*uses:\s*([^\s#]+)", text, flags=re.MULTILINE)
        assert action_refs, workflow.name
        for action_ref in action_refs:
            if action_ref.startswith("./"):
                continue
            assert re.search(r"@[0-9a-f]{40}$", action_ref), action_ref

    security = workflows[1].read_text(encoding="utf-8")
    assert "schedule:" in security
    assert "workflow_dispatch:" in security
    assert "run_simulated_soak.py --duration-seconds 1800" in security
    assert "--require-hashes --no-deps" in security
    assert "-r install/requirements.txt" in security
    assert "-r inkypi-weather/package/InkyPi/install/requirements.txt" in security
    assert security.count("python -m pip_audit") == 2
    assert (REPO_ROOT / "tools/run_simulated_soak.py").is_file()


