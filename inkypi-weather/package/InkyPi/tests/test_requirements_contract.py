def test_compiled_requirement_locks_are_self_contained_exact_and_hashed():
    for name in ("requirements-base.txt", "requirements.txt", "requirements-dev.txt"):
        path = INSTALL_ROOT / name
        text = path.read_text(encoding="utf-8")
        requirements = [
            line for line in _logical_requirements(path) if not line.startswith(("-", "--"))
        ]

        assert "pip-compile" in text, f"{name} is not generated by pip-compile"
        assert requirements, f"{name} contains no resolved distributions"
        assert all("==" in requirement for requirement in requirements), name
        assert all("--hash=sha256:" in requirement for requirement in requirements), name
        assert not any(" @ " in requirement for requirement in requirements), name
        assert not any(line.startswith("-r ") for line in _logical_requirements(path)), name


